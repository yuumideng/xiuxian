/**
 * 随机生成姓氏
 */
export function randomSurname() {
  return SURNAMES[Math.floor(Math.random() * SURNAMES.length)]
}

/**
 * 随机生成名字
 * @param {string} gender - 性别 'male' 或 'female'
 */
export function randomName(gender = 'male') {
  const names = gender === 'female' ? FEMALE_NAMES : MALE_NAMES
  return names[Math.floor(Math.random() * names.length)]
}

/**
 * 随机生成完整姓名
 * @param {string} gender - 性别 'male' 或 'female'
 */
export function randomFullName(gender = 'male') {
  return randomSurname() + randomName(gender)
}
